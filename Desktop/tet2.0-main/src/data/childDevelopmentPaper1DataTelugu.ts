export const childDevelopmentPaper1TeluguQuestions =
  rawChildDevelopmentPaper1TeluguData.map((question, index) => {
    const options = question.options ?? [];

    const normalizedCorrect = (() => {
      if (typeof question.correct === "number") {
        return question.correct;
      }

      const trimmed = (question.correct ?? "").toString().trim();
      if (!trimmed) {
        return null;
      }

      const trimmedFirstLine = trimmed.split("\n")[0]?.trim() ?? "";

      const matchedOptionIndex = options.findIndex((option) => {
        const optionTrimmed = option?.trim() ?? "";
        if (!optionTrimmed) {
          return false;
        }

        if (optionTrimmed === trimmed) {
          return true;
        }

        const optionFirstLine = optionTrimmed.split("\n")[0]?.trim() ?? "";
        return (
          optionFirstLine === trimmed ||
          optionFirstLine === trimmedFirstLine ||
          optionTrimmed === trimmedFirstLine
        );
      });

      if (matchedOptionIndex >= 0) {
        return matchedOptionIndex;
      }

      const digitsOnly = /^\d+$/;
      if (digitsOnly.test(trimmed)) {
        const potentialNumber = Number.parseInt(trimmed, 10);
        if (!Number.isNaN(potentialNumber)) {
          return potentialNumber;
        }
      }

      return null;
    })();

    const normalizedCorrectAnswer =
      typeof normalizedCorrect === "number" &&
      Number.isInteger(normalizedCorrect) &&
      normalizedCorrect >= 0 &&
      normalizedCorrect < options.length
        ? normalizedCorrect
        : 0;

    return {
      id: `TEL-CDP1-${index + 1}`,
      question: question.text ?? "",
      options,
      correctAnswer: normalizedCorrectAnswer,
      explanation: question.explanation ?? "",
    };
  });

