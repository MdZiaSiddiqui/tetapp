export const hindiPaper2Questions = rawHindiPaper2Data.map((q, index) => {
  const options = q.options ?? [];
  const correctIndex = options.indexOf(q.correct ?? '');

  return {
    id: `Q${index + 1}`,
    question: q.text,
    options,
    correctAnswer: correctIndex >= 0 ? correctIndex : 0,
    explanation: q.explanation ?? '',
  };
});


export const convertHindiPaper2ToQuestionFormat = (questions: typeof hindiPaper2Questions) => {
  return questions.map((q, index) => ({
    id: index + 1,
    question: q.question,
    options: q.options,
    correctAnswer: q.correctAnswer,
    explanation: q.explanation,
  }));
};